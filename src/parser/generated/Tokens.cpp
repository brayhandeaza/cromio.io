void Tokens::action(RuleContext *context, size_t ruleIndex, size_t actionIndex) {
  switch (ruleIndex) {
    case 35: NEWLINEAction(antlrcpp::downCast<antlr4::RuleContext *>(context), actionIndex); break;
    case 37: WS_VISIBLEAction(antlrcpp::downCast<antlr4::RuleContext *>(context), actionIndex); break;

  default:
    break;
  }
}

void Tokens::NEWLINEAction(antlr4::RuleContext *context, size_t actionIndex) {
  switch (actionIndex) {
    case 0:  if (inSkipMode && *inSkipMode) emit(); else skip();  break;

  default:
    break;
  }
}

void Tokens::WS_VISIBLEAction(antlr4::RuleContext *context, size_t actionIndex) {
  switch (actionIndex) {
    case 1:  if (inSkipMode && *inSkipMode) emit(); else skip();  break;

  default:
    break;
  }
}



